        # Node info
        local node_info
        node_info="$(kubectl get nodes -o wide --no-headers 2>/dev/null || true)"
        if [[ -n "$node_info" ]]; then
            md "| Node | Status | Roles | Version | Kernel | Container Runtime |"
            md "|------|--------|-------|---------|--------|-------------------|"
            local node_table
            node_table="$(kubectl get nodes -o wide --no-headers 2>/dev/null | awk '{
                name=$1; status=$2; roles=$3; ver=$5; kernel=$(NF-1); crt=$NF
                printf "| %s | %s | %s | %s | %s | %s |\n", name, status, roles, ver, kernel, crt
            }' || true)"
            [[ -n "$node_table" ]] && md "$node_table"
            md ""
        fi

        # Namespace pod counts
        md "### Workload Summary"
        md ""
        md "| Namespace | Running | Pending | Failed | Total |"
        md "|-----------|---------|---------|--------|-------|"

        local ns_list
        ns_list="$(kubectl get pods --all-namespaces --no-headers 2>/dev/null | awk '{print $1}' | sort -u || true)"
        while IFS= read -r ns; do
            [[ -z "$ns" ]] && continue
            local pod_lines
            pod_lines="$(kubectl get pods -n "$ns" --no-headers 2>/dev/null || true)"
            local running pending failed total
            running=$(echo "$pod_lines" | grep -c "Running" || true)
            pending=$(echo "$pod_lines" | grep -c "Pending" || true)
            failed=$(echo "$pod_lines" | grep -cE "Error|CrashLoop|Failed" || true)
            total=$(echo "$pod_lines" | grep -c . || true)
            md "| ${ns} | ${running} | ${pending} | ${failed} | ${total} |"
        done <<< "$ns_list"
        md ""

        # GPU/NPU device passthrough pods (privileged pods with device mounts)
        md "### Hardware-Accelerated Pods"
        md ""
        local hw_pods
        hw_pods="$(kubectl get pods --all-namespaces -o json 2>/dev/null | python3 -c "
import sys, json
data = json.load(sys.stdin)
results = []
for item in data.get('items', []):
    ns = item['metadata']['namespace']
    name = item['metadata']['name']
    phase = item.get('status', {}).get('phase', 'Unknown')
    devices = set()
    for c in item.get('spec', {}).get('containers', []):
        for vm in c.get('volumeMounts', []):
            mp = vm.get('mountPath', '')
            if '/dev/dri' in mp: devices.add('GPU')
            if '/dev/kfd' in mp: devices.add('ROCm')
            if '/dev/accel' in mp: devices.add('NPU')
    if devices:
        results.append((ns, name, phase, '+'.join(sorted(devices))))
for ns, name, phase, devs in results:
    print(f'| {ns} | {name} | {phase} | {devs} |')
" 2>/dev/null || true)"

        if [[ -n "$hw_pods" ]]; then
            md "| Namespace | Pod | Phase | Devices |"
            md "|-----------|-----|-------|---------|"
            md "$hw_pods"
        else
            md "> No pods with hardware device mounts detected."
        fi
        md ""

        # Resource Policies
        md "### Resource Policies"
        md ""

        # PriorityClasses (custom, non-system)
        local pc_table
        pc_table="$(kubectl get priorityclasses -o json 2>/dev/null | python3 -c "
import sys, json
data = json.load(sys.stdin)
results = []
for item in data.get('items', []):
    name = item['metadata']['name']
    if name in ('system-cluster-critical', 'system-node-critical'):
        continue
    value = item.get('value', 0)
    preempt = item.get('preemptionPolicy', 'PreemptLowerPriority')
    default = item.get('globalDefault', False)
    results.append(f'| {name} | {value} | {preempt} | {default} |')
for r in results:
    print(r)
" 2>/dev/null || true)"

        if [[ -n "$pc_table" ]]; then
            md "#### PriorityClasses (custom)"
            md ""
            md "| Name | Value | Preemption | Global Default |"
            md "|------|-------|-----------|----------------|"
            md "$pc_table"
            md ""
        else
            md "> No custom PriorityClasses defined."
            md ""
        fi

        # ResourceQuotas
        local rq_table
        rq_table="$(kubectl get resourcequotas --all-namespaces -o json 2>/dev/null | python3 -c "
import sys, json
data = json.load(sys.stdin)
for item in data.get('items', []):
    ns = item['metadata']['namespace']
    name = item['metadata']['name']
    hard = item.get('spec', {}).get('hard', {})
    limits_str = ', '.join(f'{k}={v}' for k, v in hard.items())
    print(f'| {ns} | {name} | {limits_str} |')
" 2>/dev/null || true)"

        if [[ -n "$rq_table" ]]; then
            md "#### ResourceQuotas"
            md ""
            md "| Namespace | Name | Hard Limits |"
            md "|-----------|------|-------------|"
            md "$rq_table"
            md ""
        else
            md "> No ResourceQuotas defined in any namespace."
            md ""
        fi

        # LimitRanges
        local lr_table
        lr_table="$(kubectl get limitranges --all-namespaces -o json 2>/dev/null | python3 -c "
import sys, json
data = json.load(sys.stdin)
for item in data.get('items', []):
    ns = item['metadata']['namespace']
    name = item['metadata']['name']
    for limit in item.get('spec', {}).get('limits', []):
        ltype = limit.get('type', 'Container')
        default = ', '.join(f'{k}={v}' for k, v in limit.get('default', {}).items()) or 'none'
        default_req = ', '.join(f'{k}={v}' for k, v in limit.get('defaultRequest', {}).items()) or 'none'
        print(f'| {ns} | {name} | {ltype} | {default} | {default_req} |')
" 2>/dev/null || true)"

        if [[ -n "$lr_table" ]]; then
            md "#### LimitRanges"
            md ""
            md "| Namespace | Name | Type | Default Limit | Default Request |"
            md "|-----------|------|------|---------------|-----------------|"
            md "$lr_table"
            md ""
        else
            md "> No LimitRanges defined in any namespace."
            md ""
        fi

        # Custom node labels (accelerator/*, topology/*)
        md "#### Node Labels (accelerator/topology)"
        md ""
        local node_labels
        node_labels="$(kubectl get nodes -o json 2>/dev/null | python3 -c "
import sys, json
data = json.load(sys.stdin)
for item in data.get('items', []):
    name = item['metadata']['name']
    labels = item['metadata'].get('labels', {})
    custom = {k: v for k, v in labels.items() if k.startswith(('accelerator/', 'topology/'))}
    if custom:
        for k, v in custom.items():
            print(f'| {name} | {k} | {v} |')
    else:
        print(f'| {name} | (none) | -- |')
" 2>/dev/null || true)"

        if [[ -n "$node_labels" ]]; then
            md "| Node | Label | Value |"
            md "|------|-------|-------|"
            md "$node_labels"
        else
            md "> No custom accelerator/topology labels on nodes."
        fi
        md ""

        log_ok "Kubernetes cluster accessible"
        SUMMARY_STATUS["Kubernetes"]="OK"
        SUMMARY_DETAIL["Kubernetes"]="Cluster accessible"
    elif [[ "$k8s_ready" == "true" ]]; then
        md "> kubectl found but cluster is not accessible."
        md ""
        log_warn "kubectl found but cluster unreachable"
        SUMMARY_STATUS["Kubernetes"]="WARN"
        SUMMARY_DETAIL["Kubernetes"]="kubectl found, cluster unreachable"
    else
        md "> kubectl not found. Kubernetes status cannot be checked."
        md ""
        log_warn "kubectl not found"
        SUMMARY_STATUS["Kubernetes"]="NOT FOUND"
        SUMMARY_DETAIL["Kubernetes"]="kubectl not found"
    fi
}

