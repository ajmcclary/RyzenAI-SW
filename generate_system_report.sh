                overlay_type="4x4"
            elif [[ "$xclbin_name" == *"8x4x1"* ]]; then
                overlay_type="8x4x1"
            elif [[ "$xclbin_name" == *"Nx4"* ]]; then
                overlay_type="Nx4"
