section_summary() {
    log_section "Generating summary"

    md "---"
    md ""
    md "## Summary"
    md ""
    md "| Component | Status | Details |"
    md "|-----------|--------|---------|"

    # Ordered list of components
    local components=(
        "Kernel"
        "CPU"
        "Memory"
        "Disk Space"
        "GPU"
        "VAAPI"
        "NPU"
        "NPU Device Node"
        "NPU Firmware"
        "XRT"
        "VAIP Passes"
        "Build Tools"
        "ORT C++ Libs"
        "ONNX Runtime"
        "CVML Library"
        "ROCm"
        "Ryzen AI SDK"
        "Kubernetes"
    )

    for comp in "${components[@]}"; do
        local status="${SUMMARY_STATUS[$comp]:-UNKNOWN}"
        local detail="${SUMMARY_DETAIL[$comp]:-}"
        md "| ${comp} | ${status} | ${detail} |"

        # Terminal output
        case "$status" in
            OK)             log_ok "${comp}: ${detail}" ;;
            WARN)           log_warn "${comp}: ${detail}" ;;
            NOT\ INSTALLED) log_fail "${comp}: not installed" ;;
            NOT\ CONFIGURED) log_warn "${comp}: ${detail}" ;;
            NOT\ FOUND)     log_fail "${comp}: not found" ;;
            BLOCKED)        log_fail "${comp}: ${detail}" ;;
            ERROR)          log_fail "${comp}: ${detail}" ;;
            *)              log_warn "${comp}: ${status}" ;;
        esac
    done

    md ""
    md "---"
    md ""
    md "*Generated by [RyzenAI-SW](https://github.com/amd/RyzenAI-SW) system report utility.*"
}

