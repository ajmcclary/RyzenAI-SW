            fi

            md "| \`${xclbin_name}\` | ${overlay_type} | ${xclbin_size} | \`${xclbin_uuid}\` |"
        done <<< "$xclbin_files"
        md ""

        local xclbin_count
        xclbin_count="$(echo "$xclbin_files" | wc -l)"
