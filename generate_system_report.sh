            xclbin_size="$(du -h "$xclbin_path" 2>/dev/null | awk '{print $1}')"

            # Parse overlay type from filename
            overlay_type="unknown"
            if [[ "$xclbin_name" == *"4x4"*"CFG"* ]]; then
